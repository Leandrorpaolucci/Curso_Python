primeiro_valor = int(input("Didige o valor 1: "))
segundo_valor = int(input("Digite o valor 2: "))

if primeiro_valor >= segundo_valor:
    print(f"O primeiro valor {primeiro_valor} é maior ou igual que o segundo valor {segundo_valor}")
else:
    print(f"O segundo valor {segundo_valor} é maior ou igual ao que o primeiro valor {primeiro_valor}")